def encode(message):
  retVal = ""
  message = re.sub(r"[ ]{2,}"," ",message)
  for l in message:
    retVal += toMorse(l.lower())
  return retVal.strip()

def toMorse(letter):
  if checkKey(MORSE_CODE, letter):
    return MORSE_CODE[letter] + " "
  return letter